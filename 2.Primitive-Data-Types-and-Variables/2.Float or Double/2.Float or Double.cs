using System;
class FloatorDouble
{
    static void Main()
    {
        double firstNumber = 34.567839023D;
        float secondNumber = 12.345F;
        double thirdNumber = 8923.1234857D;
        float forthNumber = 3456.091F;
        Console.WriteLine("double: {0} \nfloat: {1} \ndouble: {2} \nfloat: {3}", 
            firstNumber, secondNumber, thirdNumber,forthNumber);
    }
}

